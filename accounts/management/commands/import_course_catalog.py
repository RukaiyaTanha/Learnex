def parse_courses():
    courses = []
    for raw_line in RAW_COURSES.strip().splitlines():
        parts = [part.strip() for part in raw_line.split("\t")]
        if len(parts) != 4:
            continue
        code, name, prereq, credit = parts
        if not code or code in {"CSC****", "CSC####"}:
            continue
        courses.append(
            {
                "code": code,
                "name": name,
                "description": f"Prereq: {prereq or 'Nil'}; Credit: {credit or 'N/A'}",
            }
        )
    return courses


class Command(BaseCommand):
    help = "Import the course catalog from the pasted curriculum list."

    def handle(self, *args, **options):
        created_count = 0
        updated_count = 0

        for course_data in parse_courses():
            _, created = Course.objects.update_or_create(
                code=course_data["code"],
                defaults={
                    "name": course_data["name"],
                    "description": course_data["description"],
                },
            )
            if created:
                created_count += 1
            else:
                updated_count += 1

        self.stdout.write(
            self.style.SUCCESS(
                f"Imported {created_count} new course(s) and updated {updated_count} existing course(s)."
            )
        )